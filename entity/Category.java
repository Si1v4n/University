package entity;

public class Category {
    private Integer id;
    private String Name;

    public Integer getId() {
        return this.id;
    }

    public void setName(String name) {
        this.Name = name;
    }

    public void setId(Integer Id) {
        this.id = Id;
    }

    public String getName() {
        return Name;
    }
}
